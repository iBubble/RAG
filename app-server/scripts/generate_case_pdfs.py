import os
import fitz

os.makedirs('/app/docs', exist_ok=True)
font_path = "/usr/share/fonts/truetype/wqy/wqy-zenhei.ttc"
if not os.path.exists(font_path):
    font_path = "/usr/share/fonts/opentype/noto/NotoSansCJK-Regular.ttc"

def create_pdf(filename, title, content_lines):
    doc = fitz.open()
    page = doc.new_page(width=595, height=842)
    page.insert_text(fitz.Point(50, 60), title, fontname="zh", fontfile=font_path, fontsize=18)
    y = 120
    for line in content_lines:
        if y > 780:
            page = doc.new_page(width=595, height=842)
            y = 60
        if len(line) > 35:
            chunks = [line[i:i+35] for i in range(0, len(line), 35)]
            for chunk in chunks:
                page.insert_text(fitz.Point(50, y), chunk, fontname="zh", fontfile=font_path, fontsize=11)
                y += 20
        else:
            page.insert_text(fitz.Point(50, y), line, fontname="zh", fontfile=font_path, fontsize=11)
            y += 22
    doc.save(f'/app/docs/{filename}')
    doc.close()
    print(f"Generated {filename}")

